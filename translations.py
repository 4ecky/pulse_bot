def translate_league(league_name: str, country: str = None) -> str:
    """
    Переводит название лиги на русский

    Args:
        league_name: Название лиги на английском
        country: Страна (для уточнения)

    Returns:
        Переведенное название
    """
    # Проверяем прямое совпадение
    if league_name in LEAGUE_TRANSLATIONS:
        translated = LEAGUE_TRANSLATIONS[league_name]

        # Добавляем страну если нужно
        if country:
            # Для стран с одинаковыми названиями лиг
            if league_name == "Premier League":
                country_map = {
                    "England": "Англия",
                    "Russia": "Россия",
                    "Ukraine": "Украина"
                }
                if country in country_map:
                    return f"{translated} ({country_map[country]})"
            elif league_name == "Championship":
                country_map = {
                    "England": "Англия",
                    "Scotland": "Шотландия"
                }
                if country in country_map:
                    return f"{translated} ({country_map[country]})"

        return translated

    # Если перевода нет - возвращаем оригинал
    return league_name


def translate_team(team_name: str) -> str:
    """
    Переводит название команды на русский

    Args:
        team_name: Название команды на английском

    Returns:
        Переведенное название
    """
    return TEAM_TRANSLATIONS.get(team_name, team_name)